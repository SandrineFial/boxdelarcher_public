        <div class="card-body">
            <p><strong>La visualisation c'est quoi ?</strong></p>
            <p>Si je suis quelqu'un de visuel : en fermant les yeux, j'arrive à me voir tirer.</p>
            <p>Si je suis quelqu'un de plus auditif : je suis plutôt focalisé sur les sons, j'entend ma séquence de tir (ma voix ou celle de l'entraineur qui me dit quoi faire) sans vraiment me voir.</p>
            <p>Si je suis quelqu'un de plus kinesthésique : j'arrive à ressentir mon tir. En fermant les yeux, je me vois tirer ou pas,
            mais je sens surtout quels muscles sont mobilisés au moment de la traction et au lâcher de la corde.</p>
            <p>Tout ça c'est de la visualisation autrement appelé aussi imagerie mentale.</p>
            <p><strong>Exercices</strong></p>
            <ol>
                <li>Avant chaque flèche je ferme les yeux, pense à ma séquence de tir (sur ce que je travaille en ce moment). Au moment du lâcher je vois mon impact en cible ou ma flèche dans telle zone du blason.</li>
                <li>Et ensuite je tire réellement ma flèche.</li>
